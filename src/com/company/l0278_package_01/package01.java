// import放在package的下面，在类定义面前，可以有多句且没有顺序要求

public class package01 {
    public static void main(String[] args) {
        /*
        * 包的三大作用：
        *   1、区分相同名字的类
        *   2、当类很多时候，可以很好的管理类（参考java api文档）
        *   3、控制访问范围
        * 包的基本语法：
        *   1、package 关键字，表示打包
        *   2、com.company.package_01 表示包名
        * */
    }
}
